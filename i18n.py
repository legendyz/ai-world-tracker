def t(key: str, **kwargs) -> str:
    """
    ÁøªËØëÂáΩÊï∞
    
    Args:
        key: ÁøªËØëÈîÆ
        **kwargs: Ê†ºÂºèÂåñÂèÇÊï∞
        
    Returns:
        ÁøªËØëÂêéÁöÑÂ≠óÁ¨¶‰∏≤
    """
    lang_pack = LANG_PACKS.get(_current_language, LANG_PACKS['en'])
    text = lang_pack.get(key, key)
